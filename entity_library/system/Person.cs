public class Person
{
    private int idPerson;
    private string name = "";
    private string lastName = "";

    public int IdPerson
    {
        get { return idPerson; }
        set { idPerson = value; }
    }
    public string Name
    {
        get { return name; }
        set { name = value; }
    }
    public string LastName
    {
        get { return lastName; }
        set { lastName = value; }
    }
}    