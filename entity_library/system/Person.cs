public class Person
{
    private long id;
    private string name = "";
    private string lastName = "";

    public long Id
    {
        get { return id; }
        set { id = value; }
    }
    public string Name
    {
        get { return name; }
        set { name = value; }
    }
    public string LastName
    {
        get { return lastName; }
        set { lastName = value; }
    }
}    